public class SoilData {

    int nitrogen;
    int phosphorus;
    int potassium;
    double ph;
    String crop;

    public SoilData(int nitrogen, int phosphorus, int potassium, double ph, String crop) {
        this.nitrogen = nitrogen;
        this.phosphorus = phosphorus;
        this.potassium = potassium;
        this.ph = ph;
        this.crop = crop;
    }
}
